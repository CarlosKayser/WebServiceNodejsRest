# WebServiceNodejsRest
Web Service REST desenvolvido para disciplina de Serviços Web - IFSul - Passo Fundo

O roteamento do webService foi implementado utilizando o módulo Express (https://expressjs.com/pt-br/). A auntenticação foi realizada utilizando JWT (JSON Web Tokens), para tal foi utilizado o módulo para nodejs JWT.IO (https://jwt.io/).

